let a = +prompt('Digite um número'), b = +prompt('Digite outro número');
alert(`Soma: ${a + b}\nDivisão é: ${a / b}\nMultiplicação é: ${a * b}\nSubtração é: ${a - b}\nResto é: ${a % b}\nFIM`);

/* 
Usei variáveis a e b para simplificar os nomes.
O +prompt() já converte a entrada para número automaticamente.
Juntei todas as operações em um único alert() usando \n para quebra de linha. 
*/