let PriNumero = prompt('Digite um número');
PriNumero = parseInt(PriNumero);
let SegNumero = prompt('Digite outro número');
SegNumero = parseInt(SegNumero);

alert(`A soma é: ${PriNumero + SegNumero}`);
alert(`a divisão é: ${PriNumero / SegNumero}`);
alert(`a multiplicação é: ${PriNumero * SegNumero}`);
alert(`a subtração é: ${PriNumero - SegNumero}`);
alert(`o resto é ${PriNumero % SegNumero}`)
